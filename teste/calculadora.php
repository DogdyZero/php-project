<?php 
    class Calculadora{

        public function soma($valorA, $valorB){
            return $valorA+$valorB;
        }
        public function subtracao($valorA, $valorB){
            return $valorA-$valorB;
        }
        public function multiplicacao($valorA, $valorB){
            return $valorA*$valorB;
        }
        public function divisao($valorA, $valorB){
            return $valorA/$valorB;
        }
    }
?>